package com.library.entity;

public class BookTag {
    private int bookId;
    private int tagId;
    
    // Getters and Setters
    public int getBookId() {
        return bookId;
    }
    
    public void setBookId(int bookId) {
        this.bookId = bookId;
    }
    
    public int getTagId() {
        return tagId;
    }
    
    public void setTagId(int tagId) {
        this.tagId = tagId;
    }
}

